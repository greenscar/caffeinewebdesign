package vos;

public class OrderQuantityVO {
    private int id;
    private String name;
    /** Creates a new instance of OrderQuantityVO */
    public OrderQuantityVO() {
    }
    public OrderQuantityVO(int i, String n) {
        this.id = i;
        this.name = n;
    }
    public void setID(int id)
    {
        this.id = id;
    }
    public void setName(String n)
    {
        this.name = n;
    }
    public int getID()
    {
        return this.id;
    }
    public String getName()
    {
        return this.name;
    }
    
}
